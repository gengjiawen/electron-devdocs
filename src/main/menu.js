function openAboutDialog() {
  const info = `Version ${app.getVersion()}
Electron ${process.versions['electron']}
Renderer ${process.versions['chrome']}
Node ${process.versions['node']}`

  dialog.showMessageBox({
    title: 'Electron Devdocs',
    type: 'info',
    message: `${app.getName()}`,
    detail: info,
    noLink: true,
  })
}

